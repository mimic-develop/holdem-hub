export function getConceptsByCategory(slug: string, difficulty?: Difficulty): Concept[] {
  const categoryConcepts = CONCEPTS[slug as CategorySlug];
  if (!categoryConcepts) return [];

  if (difficulty) {
    return categoryConcepts[difficulty] ?? [];
  }

  return categoryConcepts.club ?? [];
}
